// Helper: parse terrain strings into 2D array, auto-pad to 80 cols
function parseTerrain(rows) {
    return rows.map(row => {
        // Pad short rows with the last character used
        while (row.length < 80) row += row[row.length - 1];
        return row.slice(0, 80).split('');
    });
}

